    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Connessione ad Arduino
        //se l'ip è vuoto, avviso l'utente di inserire l'IP
   if(ip == null || ip.equalsIgnoreCase("")) JOptionPane.showConfirmDialog(null,"Inserisci prima l'IP","Attenzione!",JOptionPane.OK_CANCEL_OPTION,JOptionPane.WARNING_MESSAGE);
   if(ip.equalsIgnoreCase("") == false && primo == true){  //se l'IP è valido ed è la prima volta che accedo
       d.tConn(ip);//imposto l'IP
       d.open();//apro la connessione
       primo = false;//imposto primo accesso a false
       return;
   }
    //se non è il primo accesso e il programma è disconnesso, apro la connessione
    if(primo == false && d.isConnected() == false){d.open();}
    }//GEN-LAST:event_jButton1ActionPerformed

