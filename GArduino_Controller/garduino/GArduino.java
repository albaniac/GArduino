    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        //gestione automatica della luce
    if(!d.isConnected()){//se il programma non è connesso, avviso l'utente
        JOptionPane.showConfirmDialog(null,"Programma non connesso!","Attenzione!",JOptionPane.OK_CANCEL_OPTION,JOptionPane.WARNING_MESSAGE);
        return;
    }
       if(!auto){ auto = true;jLabel11.setText("Abilitato");}//automatico è false, imposto a true e scrivo sulla etichettà che è abilitato
       else{ auto = false; jLabel11.setText("Disabilitato"); }//se invece è in automatico, imposto la gestione in manuale e scrivo sulla etichetta che è disabilitato
        d.auto(auto);//richiedo la gestione in automatico/manuale
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // Inserisci l'IP
        ip =""; //inizializzo IP a ""
        JFrame f = new JFrame("Inserisci l'IP");//creo il frame per il pop-up
     while(ip.equalsIgnoreCase("")){//finche l'ip è vuoto
        ip  = JOptionPane.showInputDialog(f,"Inserisci l'IP di Arduino");//richiedo di inserire l'IP
        if(ip == null){ ip = ""; break;}//se l'IP uguale a null, lo inizializzo e esco
               if(!ip.matches("\\b(25[0-5]|2[0-4]\\d|1\\d\\d|[1-9]?\\d)(\\.(25[0-5]|2[0-4]\\d|1\\d\\d|[1-9]?\\d)){3}\\b")){//se la stringa inserita non è un indirizzo IPv4
                    JOptionPane.showConfirmDialog(null,"IP non valido!","Attenzione!",JOptionPane.OK_CANCEL_OPTION,JOptionPane.WARNING_MESSAGE);//avviso l'utente
                ip = "";//svuoto l'IP
                return;
               }
     }
    }//GEN-LAST:event_jButton7ActionPerformed

